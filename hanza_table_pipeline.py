for file_name in sorted(os.listdir(image_dir)):
    if file_name.lower().endswith(('.png', '.jpg', '.jpeg')):
        img_path = os.path.join(image_dir, file_name)
        txt_path = os.path.join(output_dir, file_name.rsplit('.', 1)[0] + ".md")
        
        image_to_markdown_table(img_path, txt_path)
        # API Rate Limit 방지를 위해 1~2초 대기

def extract_images_from_hwpx(hwpx_path, output_dir):
    """
    HWPX 파일(사실상 ZIP 압축 파일)에서 무손실로 BinData 폴더 내 원본 이미지를 추출합니다.
    """
    logging.info(f"HWPX 이미지 추출 시작: {hwpx_path}")
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)
        
    try:
        extracted_count = 0
        with zipfile.ZipFile(hwpx_path, 'r') as z:
            # HWPX 내부의 Contents/BinData/ 경로에 있는 이미지 탐색
            bin_files = [f for f in z.namelist() if 'Contents/BinData/' in f]
            
            if not bin_files:
                logging.warning("HWPX 파일 내에 Contents/BinData/ 경로 또는 이미지가 존재하지 않습니다.")
                return 0
                
            for file_path in bin_files:
                filename = os.path.basename(file_path)
                if not filename:  # 디렉토리 제외
                    continue
                
                # 이미지 확장자 검증
                ext = os.path.splitext(filename)[1].lower()
                if ext in ['.png', '.jpg', '.jpeg', '.gif', '.bmp', '.tiff']:
                    target_path = os.path.join(output_dir, filename)
                    with z.open(file_path) as source, open(target_path, 'wb') as target:
                        shutil.copyfileobj(source, target)
                    extracted_count += 1
                    
        logging.info(f"HWPX 추출 완료! 총 {extracted_count}개의 이미지가 '{output_dir}'에 저장되었습니다.")
        return extracted_count
    except Exception as e:
        logging.error(f"HWPX 추출 중 에러 발생: {e}")
        return 0


def extract_images_from_pdf(pdf_path, output_dir):
    """
    PyMuPDF(fitz)를 사용하여 PDF 내의 모든 이미지 객체를 무손실로 추출합니다.
    """
    logging.info(f"PDF 이미지 추출 시작: {pdf_path}")
    try:
        import fitz  # PyMuPDF
    except ImportError:
        logging.error("PyMuPDF 라이브러리가 설치되지 않았습니다. 'pip install pymupdf'를 실행하세요.")
        return 0

    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    try:
        doc = fitz.open(pdf_path)
        img_count = 0
        
        for page_num in range(len(doc)):
            page = doc[page_num]
            image_list = page.get_images(full=True)
            
            for img_idx, img in enumerate(image_list):
                xref = img[0]
                base_image = doc.extract_image(xref)
                image_bytes = base_image["image"]
                image_ext = base_image["ext"]  # png, jpeg 등
                
                # 파일명 포맷: page_001_img_01.png
                img_name = f"page_{page_num + 1:03d}_img_{img_idx + 1:02d}.{image_ext}"
                img_path = os.path.join(output_dir, img_name)
                
                with open(img_path, "wb") as f:
                    f.write(image_bytes)
                img_count += 1
                
        logging.info(f"PDF 추출 완료! 총 {img_count}개의 이미지가 '{output_dir}'에 저장되었습니다.")
        return img_count
    except Exception as e:
        logging.error(f"PDF 추출 중 에러 발생: {e}")
        return 0


def process_images_with_gemini(image_dir, output_dir, api_key):
    """
    Gemini 1.5 Flash API를 활용하여 추출된 이미지들을 Markdown Table 구조로 자동 변환합니다.
    """
    logging.info("Gemini 1.5 Flash API를 이용한 표 변환 작업을 시작합니다.")
    try:
        import google.generativeai as genai
        from tqdm import tqdm
    except ImportError:
        logging.error("필수 라이브러리가 누락되었습니다. 'pip install google-generativeai tqdm'을 실행하세요.")
        return

    if not api_key:
        logging.error("Gemini API 키가 제공되지 않았습니다. --api-key 인자를 입력해 주세요.")
        return

    # API 설정
    genai.configure(api_key=api_key)
    model = genai.GenerativeModel('gemini-1.5-flash')

    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    # 이미지 파일 수집
    valid_extensions = ('.png', '.jpg', '.jpeg', '.bmp', '.tiff')
    image_files = sorted([f for f in os.listdir(image_dir) if f.lower().endswith(valid_extensions)])

    if not image_files:
        logging.warning(f"'{image_dir}' 폴더에 변환할 이미지 파일이 없습니다.")
        return

    # 한자 및 표 레이아웃 인식을 위한 최적화된 프롬프트
    prompt = """
    이 이미지는 한국 정부 또는 공공기관에서 발행한 공식 '인명용한자표'의 일부입니다.
    이미지 내의 표 구조를 정밀하게 분석하여 누락되는 행/열 없이 Markdown 테이블 형식으로 완벽하게 변환해 주세요.

    [작성 규칙]
    1. 한자(번체자/정체자)가 획수가 많아 깨져 보일 수 있으니 문맥(한글 음/뜻)과 대조하여 원본 한자 그대로 정확하게 텍스트화하세요.
    2. 표의 원래 헤더 구조(예: 연번/순번, 한자, 한글 음, 뜻/부수, 획수 등)를 그대로 유지하세요.
    3. 병합된 셀이 있다면, 데이터 무결성을 위해 논리적으로 셀 값을 해체하여 각 행에 반복 채워주세요.
    4. 마크다운 테이블 코드 블록(```markdown ... ```) 형식으로만 결과를 출력하고, 추가적인 인사말이나 설명은 절대 생략하세요.
    """

    for file_name in tqdm(image_files, desc="Gemini OCR 변환 중"):
        img_path = os.path.join(image_dir, file_name)
        txt_path = os.path.join(output_dir, os.path.splitext(file_name)[0] + ".md")

        # 이미 변환된 파일은 스킵 (이어받기 기능 지원)
        if os.path.exists(txt_path):
            continue

        retries = 3
        while retries > 0:
            try:
                image = Image.open(img_path)
                response = model.generate_content([prompt, image])
                
                # 결과 텍스트 저장
                with open(txt_path, "w", encoding="utf-8") as f:
                    f.write(response.text)
                
                # Rate Limit 예방을 위한 대기
                time.sleep(1.5)
                break
            except Exception as e:
                retries -= 1
                logging.warning(f"{file_name} 처리 중 오류 발생 (남은 시도: {retries}회): {e}")
                time.sleep(5)
                if retries == 0:
                    logging.error(f"{file_name} 변환 실패.")


def parse_markdown_table_to_df(md_text):
    """
    마크다운 텍스트에서 표 부분을 찾아 Pandas DataFrame으로 파싱합니다.
    """
    import pandas as pd
    
    # 마크다운 코드 블록 제거 및 표 매칭
    table_lines = []
    in_table = False
    
    # 정규식으로 마크다운 표 패턴 추출
    lines = md_text.strip().split('\n')
    for line in lines:
        cleaned = line.strip()
        if cleaned.startswith('|') and cleaned.endswith('|'):
            # 구분선(|---|---|...)은 데이터에서 제외
            if re.match(r'^\|\s*:?-+:?\s*(?:\|\s*:?-+:?\s*)*\|$', cleaned):
                continue
            # 셀 분해 후 공백 제거
            cells = [c.strip() for c in cleaned.split('|')[1:-1]]
            table_lines.append(cells)
            
    if not table_lines:
        return None
        
    try:
        # 첫 번째 행을 헤더로 설정
        headers = table_lines[0]
        data = table_lines[1:]
        
        # 열 개수 맞추기 (데이터 정합성 보장)
        max_cols = len(headers)
        cleaned_data = []
        for row in data:
            if len(row) < max_cols:
                row = row + [''] * (max_cols - len(row))
            elif len(row) > max_cols:
                row = row[:max_cols]
            cleaned_data.append(row)
            
        return pd.DataFrame(cleaned_data, columns=headers)
    except Exception as e:
        logging.error(f"테이블 파싱 오류: {e}")
        return None


def merge_and_export_results(markdown_dir, output_excel_path):
    """
    각 개별 마크다운 파일을 읽어 하나의 통합 엑셀(Excel)/CSV 파일로 생성합니다.
    """
    logging.info("개별 변환 결과 통합 및 검증 작업을 시작합니다.")
    try:
        import pandas as pd
    except ImportError:
        logging.error("Pandas 라이브러리가 필요합니다. 'pip install pandas openpyxl'을 실행하세요.")
        return

    md_files = sorted([f for f in os.listdir(markdown_dir) if f.endswith('.md')])
    if not md_files:
        logging.warning("통합할 마크다운 파일이 존재하지 않습니다.")
        return

    all_dfs = []
    for file_name in md_files:
        file_path = os.path.join(markdown_dir, file_name)
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
            
        df = parse_markdown_table_to_df(content)
        if df is not None and not df.empty:
            # 원본 파일명 보존 컬럼 추가 (추적용)
            df['원천파일명'] = file_name
            all_dfs.append(df)
            
    if all_dfs:
        try:
            # 병합 진행
            merged_df = pd.concat(all_dfs, ignore_index=True)
            
            # 컬럼 전처리 (공백 제거 및 기본 정제)
            merged_df.columns = [col.strip() for col in merged_df.columns]
            
            # 최종 결과 파일 저장
            if output_excel_path.endswith('.xlsx'):
                merged_df.to_excel(output_excel_path, index=False)
            else:
                merged_df.to_csv(output_excel_path, index=False, encoding='utf-8-sig')
                
            logging.info(f"통합 완료! 파일이 성공적으로 저장되었습니다: {output_excel_path}")
            logging.info(f"총 데이터 수량(행): {len(merged_df)}개")
        except Exception as e:
            logging.error(f"병합 및 파일 저장 중 오류 발생: {e}")
    else:
        logging.warning("유효하게 파싱된 표 데이터가 없습니다.")


def main():
    parser = argparse.ArgumentParser(description="HWPX/PDF 내 한자 표 이미지 추출 및 Gemini OCR 파이프라인")
    
    parser.add_argument('--input', required=True, help="입력 HWPX 또는 PDF 파일 경로")
    parser.add_argument('--ext-dir', default="./extracted_images", help="추출된 이미지가 저장될 디렉토리 (기본: ./extracted_images)")
    parser.add_argument('--md-dir', default="./markdown_results", help="Gemini 변환 마크다운 파일 저장 디렉토리 (기본: ./markdown_results)")
    parser.add_argument('--output', default="./final_hanja_table.xlsx", help="최종 통합 파일 경로 (csv 또는 xlsx 권장, 기본: ./final_hanja_table.xlsx)")
    parser.add_argument('--api-key', help="Google Gemini API Key (환경변수 GEMINI_API_KEY 로도 설정 가능)")
    
    args = parser.parse_args()
    
    # API 키 확인 우선순위: Argument > 환경변수
    api_key = args.api_key or os.environ.get("GEMINI_API_KEY")

    # 1. 문서 타입 분석 후 이미지 추출
    input_path = args.input
    if not os.path.exists(input_path):
        logging.error(f"입력 파일이 존재하지 않습니다: {input_path}")
        sys.exit(1)
        
    ext = os.path.splitext(input_path)[1].lower()
    img_count = 0
    
    if ext == '.hwpx':
        img_count = extract_images_from_hwpx(input_path, args.ext_dir)
    elif ext == '.pdf':
        img_count = extract_images_from_pdf(input_path, args.ext_dir)
    else:
        logging.error("지원되지 않는 파일 포맷입니다. (.hwpx 또는 .pdf 파일만 지원)")
        sys.exit(1)
        
    if img_count == 0:
        logging.warning("추출된 이미지가 없습니다. 작업을 종료합니다.")
        sys.exit(0)

    # 2. Gemini를 통한 OCR 및 마크다운 표 변환 진행
    if not api_key:
        logging.warning("Gemini API Key가 누락되어 OCR 변환을 진행하지 않고 이미지만 추출한 후 종료합니다.")
        logging.warning("OCR 변환을 계속하려면 '--api-key'를 입력하거나 'GEMINI_API_KEY' 환경변수를 등록해 주세요.")
        sys.exit(0)
        
    process_images_with_gemini(args.ext_dir, args.md_dir, api_key)
    
    # 3. 데이터 수집 및 엑셀 통합
    merge_and_export_results(args.md_dir, args.output)

if __name__ == "__main__":
    main()
